    private void tableBayarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBayarMouseClicked
        int clickedRow = tableBayar.getSelectedRow();
        TableModel tableModel = tableBayar.getModel();
        selectedIDTransaksi = Integer.parseInt(tableModel.getValueAt(clickedRow, 7).toString());
        setTableObat();
        setTableTindakan();
        setPayDelBtn(true);
    }//GEN-LAST:event_tableBayarMouseClicked

    private void tableObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableObatMouseClicked
        tableTindakan.clearSelection();
        int clickedRow = tableObat.getSelectedRow();
        TableModel tableModel = tableObat.getModel();
        selectedIDDetailTransaksi = Integer.parseInt(tableModel.getValueAt(clickedRow, 4).toString());
        selectedPerawatan = tableModel.getValueAt(clickedRow, 1).toString();
        jumlahObatSpinner.setValue(Integer.parseInt(tableModel.getValueAt(clickedRow, 2).toString()));
    }//GEN-LAST:event_tableObatMouseClicked

