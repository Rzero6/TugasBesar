    private void setTableBayar(String query) {
        tableBayar.setModel(transaksiControl.showDataPembayaranCustomer(query, "Bayar"));
    }

    private void setListener() {
        searchTxt.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                setTableBayar(searchTxt.getText());
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                setTableBayar(searchTxt.getText());
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                setTableBayar(searchTxt.getText());
            }
        });
        
        tableObat.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    delBtn.setEnabled(tableObat.getSelectedRow() != -1 || tableTindakan.getSelectedRow() != -1);
                    jumlahObatSpinner.setEnabled(tableObat.getSelectedRow() != -1);
                    editBtn.setEnabled(tableObat.getSelectedRow() != -1);
                }
            }
        });
        tableTindakan.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    delBtn.setEnabled(tableObat.getSelectedRow() != -1 || tableTindakan.getSelectedRow() != -1);
                }
            }
        });
        jumlahObatSpinner.addChangeListener(new ChangeListener() {
            @Override
            public void stateChanged(ChangeEvent e) {
                int value = (int) jumlahObatSpinner.getValue();
                if (value < 1) {
                    jumlahObatSpinner.setValue(1);
                }
            }
        });
    }

    private void setPayDelBtn(boolean b) {
        payBtn.setEnabled(b);
        detailBtn.setEnabled(b);
    }

    private void setTableObat( ) {
        tableObat.setModel(detailTransaksiControl.showDataKeranjangObat("", selectedIDTransaksi));
        UtilTable.tableResizeColumnWidth(tableObat);
    }

    private void setTableTindakan( ) {
        tableTindakan.setModel(detailTransaksiControl.showDataKeranjangTindakan("", selectedIDTransaksi));
        UtilTable.tableResizeColumnWidth(tableTindakan);
    }

    private String getCurrentDateAndTime() {
        LocalDateTime currentDateTime = LocalDateTime.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String sqlDateandTime = currentDateTime.format(formatter);
        return sqlDateandTime;
    }
