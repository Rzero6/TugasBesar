    private void setTableObat(String query) {
        tableObat.setModel(obatControl.showDataObat(query));
        listObat = obatControl.showListObat("");
    }

    private void setListener() {
        searchTxt.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                setTableObat(searchTxt.getText());
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                setTableObat(searchTxt.getText());
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                setTableObat(searchTxt.getText());
            }
        });
        DocumentListener docListener = new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                checkFields();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                checkFields();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                checkFields();
            }

            
        };
        namaObatTxt.getDocument().addDocumentListener(docListener);
        hargaObatTxt.getDocument().addDocumentListener(docListener);
        
        jumlahObatSpinner.addChangeListener(new ChangeListener() {
            @Override
            public void stateChanged(ChangeEvent e) {
                int value = (int) jumlahObatSpinner.getValue();
                if(value<0){
                    jumlahObatSpinner.setValue(0);
                }
                checkFields();
            }
        });
        
        tableObat.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(!e.getValueIsAdjusting()){
                    checkFields();
                }
            }
        });
    }

    @Override
    public void refreshData() {
        setTableObat("");
        clearAll();
    }
    
    private void checkFields() {
        try{
            inputKosongException();
            int jumlah = (int) jumlahObatSpinner.getValue();
            Double harga = Double.parseDouble(hargaObatTxt.getText());
            addBtn.setEnabled(jumlah>0&&harga>0 && tableObat.getSelectedRow()==-1);
            editBtn.setEnabled(jumlah>0&&harga>0 && tableObat.getSelectedRow()!=-1);
            delBtn.setEnabled(tableObat.getSelectedRow()!=-1);
        }catch(InputKosongException ex){
            addBtn.setEnabled(false);
            editBtn.setEnabled(false);
        }catch(NumberFormatException nfe){
            addBtn.setEnabled(false);
            editBtn.setEnabled(false);
        }
        catch(Exception e){
            addBtn.setEnabled(false);
            editBtn.setEnabled(false);
        }
    }
    
    private void inputKosongException() throws InputKosongException{
        if(namaObatTxt.getText().isEmpty() || hargaObatTxt.getText().isEmpty()){
            throw new InputKosongException();
        }
    }

    private void clearAll() {
        tableObat.clearSelection();
        searchTxt.setText("");
        namaObatTxt.setText("");
        hargaObatTxt.setText("");
        jumlahObatSpinner.setValue(0);
        addBtn.setEnabled(false);
        editBtn.setEnabled(false);
        delBtn.setEnabled(false);
    }
    
    private Boolean checkObatSama(Obat obat){
        for (Obat o : listObat){
            if(o.getNama().equalsIgnoreCase(obat.getNama())){
                return true;
            }
        }
        return false;
    }
    private void setBorderandFocus(JComponent comp){
        comp.putClientProperty(FlatClientProperties.STYLE, ""
                +"borderWidth:1;"
                + "focusWidth:0");
    }
