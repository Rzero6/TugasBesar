    private void setSupplierTable() {
        supplierTable.setModel(supplierControl.getTableSuplier(""));
    }

    private void setEditDelBtn(boolean b) {
        editBtn.setEnabled(b);
        delBtn.setEnabled(b);
    }

    private void inputKosongException() throws InputKosongException{
        if(namaTxt.getText().isEmpty() || noTeleponTxt.getText().isEmpty() || emailTxt.getText().isEmpty()){
            throw new InputKosongException();
        }
    }

    private void clearAll() {
        namaTxt.setText("");
        noTeleponTxt.setText("");
        emailTxt.setText("");
        setEditDelBtn(false);
    }
