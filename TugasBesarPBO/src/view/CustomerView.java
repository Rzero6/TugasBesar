    private void tableCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCustomerMouseClicked
        // TODO add your handling code here:
        setEditDeleteBtn(true);
        int clickRow = tableCustomer.getSelectedRow();
        TableModel tableModel = tableCustomer.getModel();
        
        selectedId= Integer.parseInt(tableModel.getValueAt(clickRow, 0).toString());
        namaInput.setText(tableModel.getValueAt(clickRow, 1).toString());
        noTeleponInput.setText(tableModel.getValueAt(clickRow, 2).toString());
        alamatInput.setText(tableModel.getValueAt(clickRow, 3).toString());
        String sqlDate = tableModel.getValueAt(clickRow, 4).toString();
        tanggalLahir.setDate(stringToDate(sqlDate));
    }//GEN-LAST:event_tableCustomerMouseClicked

