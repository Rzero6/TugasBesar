    private void InputKosongException() throws InputKosongException{
        if(namaInput.getText().isEmpty() || noTeleponInput.getText().isEmpty() 
        || alamatInput.getText().isEmpty()){
            throw new InputKosongException();
        }
    }

    private void showCustomer() {
        tableCustomer.setModel(customerControl.showDataCustomer(""));
    }
    
    private Date stringToDate(String value){
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date date = new Date();
        try{
            date = sdf.parse(value);
        }catch(Exception e){
            System.out.println("Error: "+e.getMessage());
        }
        return date;
    }
    private String dateToString(Date value){
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        return (String) sdf.format(value);
    }
